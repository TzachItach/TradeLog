export type TKeys = keyof typeof translations.he;
export type Translations = typeof translations.he;

export function useT(lang: Language) {
  return translations[lang] as Translations;
}

export function t(lang: Language, key: TKeys): string {
  const val = (translations[lang] as Record<string, unknown>)[key];
  if (typeof val === 'string') return val;
  return key;
}
